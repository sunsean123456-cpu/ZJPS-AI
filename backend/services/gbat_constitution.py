def get_confidence_level(confidence: float) -> str:
    """根据置信度返回可信等级"""
    if confidence >= 0.9:
        return "A"
    elif confidence >= 0.75:
        return "B"
    elif confidence >= 0.5:
        return "C"
    elif confidence >= 0.25:
        return "D"
    else:
        return "E"

